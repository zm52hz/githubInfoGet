package com.zml.myclass;

public class GithubProject {
	//github项目ID
	private String idGit;
	
	//github项目名称
	private String nameGit;
	
	//github项目star数
	private int starNumGit;

	public String getIdGit() {
		return idGit;
	}

	public void setIdGit(String idGit) {
		this.idGit = idGit;
	}

	public String getNameGit() {
		return nameGit;
	}

	public void setNameGit(String nameGit) {
		this.nameGit = nameGit;
	}

	public int getStarNumGit() {
		return starNumGit;
	}

	public void setStarNumGit(int starNumGit) {
		this.starNumGit = starNumGit;
	}

}
